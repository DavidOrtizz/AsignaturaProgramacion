public class OperadorLogicoNot {
	
	public static void main (String[] args) {
		boolean a, b, c, d;
		
		a = true;
		b = true;
		c = false;
		d = false;
		
		System.out.println("No true = " +!a);
		System.out.println("No true = " +!b);
		System.out.println("No false = "+!c);
		System.out.println("No false = "+!d);
	}
}
